    @Override
    public void onInitNaviSuccess() {
        // 构造起点POI
        NaviPoi fromPoi = new NaviPoi("起点", new LatLng(39.925846, 116.435765), "");
        // 构造终点POI
        NaviPoi toPoi = new NaviPoi("终点", new LatLng(39.925846, 116.532765), "");

        boolean ok = aMapNavi.calculateEleBikeRoute(fromPoi, toPoi, TravelStrategy.SINGLE);
        if (ok) {
            Log.i("BikeRoute", "calculate route success");
        } else {
            Log.e("BikeRoute", "failed to calculate routes");
        }
    }

